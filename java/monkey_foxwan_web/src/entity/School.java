package entity;

public class School {
	private String cityId;
	private String schooName;

	public String getCityId() {
		return cityId;
	}

	public void setCityId(String cityId) {
		this.cityId = cityId;
	}

	public String getSchooName() {
		return schooName;
	}

	public void setSchooName(String schooName) {
		this.schooName = schooName;
	}
}
