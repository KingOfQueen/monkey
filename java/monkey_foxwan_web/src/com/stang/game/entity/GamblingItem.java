package com.stang.game.entity;

public class GamblingItem {
	private int id;
	private int mId;
	private int typeId;
	private int cost;
	private int is_rare;
	private int rare_num;
	private int rare_num_now;
	private int flag;
	private int is_show;
	private int type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMId() {
		return mId;
	}
	public void setMId(int id) {
		mId = id;
	}
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getIs_rare() {
		return is_rare;
	}
	public void setIs_rare(int is_rare) {
		this.is_rare = is_rare;
	}
	public int getRare_num() {
		return rare_num;
	}
	public void setRare_num(int rare_num) {
		this.rare_num = rare_num;
	}
	public int getRare_num_now() {
		return rare_num_now;
	}
	public void setRare_num_now(int rare_num_now) {
		this.rare_num_now = rare_num_now;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	public int getIs_show() {
		return is_show;
	}
	public void setIs_show(int is_show) {
		this.is_show = is_show;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
}
