package com.stang.game.ffd.entity.detail;

import java.util.Date;

public class EntityGameChatRoleDetail {
	private Integer id;
	private Integer roleSite;
	private Integer roomNo;
	private Integer battleTroop;
	private Date createTime;
	private Integer serverId;
	private Integer flag;
	private Integer roomArea;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getRoleSite() {
		return roleSite;
	}
	public void setRoleSite(Integer roleSite) {
		this.roleSite = roleSite;
	}
	public Integer getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(Integer roomNo) {
		this.roomNo = roomNo;
	}
	public Integer getBattleTroop() {
		return battleTroop;
	}
	public void setBattleTroop(Integer battleTroop) {
		this.battleTroop = battleTroop;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getServerId() {
		return serverId;
	}
	public void setServerId(Integer serverId) {
		this.serverId = serverId;
	}
	public Integer getFlag() {
		return flag;
	}
	public void setFlag(Integer flag) {
		this.flag = flag;
	}
	public Integer getRoomArea() {
		return roomArea;
	}
	public void setRoomArea(Integer roomArea) {
		this.roomArea = roomArea;
	}
	
}
