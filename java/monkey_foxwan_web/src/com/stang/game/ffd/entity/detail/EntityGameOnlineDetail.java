package com.stang.game.ffd.entity.detail;

public class EntityGameOnlineDetail {
		private int onlineNum=0;//在线人数
		private int battleNum=0;//战斗人数
		private int roomNum=0;//房间人数
		private int chageArea=0;//选取界面
		public int getOnlineNum() {
			return onlineNum;
		}
		public void setOnlineNum(int onlineNum) {
			this.onlineNum = onlineNum;
		}
		public int getBattleNum() {
			return battleNum;
		}
		public void setBattleNum(int battleNum) {
			this.battleNum = battleNum;
		}
		public int getRoomNum() {
			return roomNum;
		}
		public void setRoomNum(int roomNum) {
			this.roomNum = roomNum;
		}
		public int getChageArea() {
			return chageArea;
		}
		public void setChageArea(int chageArea) {
			this.chageArea = chageArea;
		}
}
