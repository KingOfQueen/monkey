package com.stang.game.ffd.entity.detail;

public class EntityGameJobDetail {
	protected int strength;//力量
	protected int agility;//敏捷
	protected int defanse;//防御
	public int getStrength() {
		return strength;
	}
	public void setStrength(int strength) {
		this.strength = strength;
	}
	public int getAgility() {
		return agility;
	}
	public void setAgility(int agility) {
		this.agility = agility;
	}
	public int getDefanse() {
		return defanse;
	}
	public void setDefanse(int defanse) {
		this.defanse = defanse;
	}
}
